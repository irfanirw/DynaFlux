using DynaFluxCore;
namespace DynaFlux.Nodes;

public static class Arithmetic
{
    /// <summary>
    /// Adds two numbers.
    /// </summary>
    /// <param name="a">First addend.</param>
    /// <param name="b">Second addend.</param>
    /// <returns>Sum of a and b.</returns>
    public static double Addition(double a, double b)
    {
        return a + b;
    }
}
